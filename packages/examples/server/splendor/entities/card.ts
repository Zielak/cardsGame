import { BaseCard, decorators, IBaseCardOptions } from "@cardsgame/server"

export class Card extends BaseCard {
  type = "card"
  level: number

  @decorators.condvis
  costD: number // White

  @decorators.condvis
  costS: number // Blue

  @decorators.condvis
  costE: number // Green

  @decorators.condvis
  costR: number // Red

  @decorators.condvis
  costO: number // Black

  @decorators.condvis
  gem: Gems

  @decorators.condvis
  vp: number

  constructor(options: ICardOptions) {
    super(options)
  }
}

export interface ICardOptions extends IBaseCardOptions {
  costD: number
  costS: number
  costE: number
  costR: number
  costO: number
  gem: Gems
  vp: number
}

export enum Gems {
  Diamond,
  Sapphire,
  Emerald,
  Ruby,
  Onyx,
  Gold
}

